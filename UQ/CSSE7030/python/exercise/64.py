import turtle
turtle.showturtle()#尖头
turtle.write('hello world')#打字
turtle.forward(300)#向前移动
turtle.color('red')#线改红色
turtle.left(90)#向右转90度
turtle.forward(300)#前进300
turtle.goto(0,50)#走到x=0，y=50
turtle.goto(0,0)#走到x=0，y=0
turtle.penup()#抬起笔
turtle.goto(0,300)#走到x=0，y=300
turtle.pendown()#笔放下
turtle.goto(100,100)
turtle.circle(100)#画圆




turtle.exitonclick()#停下